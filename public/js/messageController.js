app.controller('roomCtrl',function($scope,socket,$state){
    $scope.messages=[];
    /*if(!window.localStorage.IOpusUser){
        $state.go('login')
    }else{
        $state.go('startChat')
    }*/
    $state.go('login');
    socket.on('messageAdded',function(msgs){
        $scope.$apply(
            function(){
                $scope.messages=msgs;
            }
        )
    });
    socket.on('allMessages',function(msgs){
        $scope.$apply(
            function(){
                $scope.messages=msgs;
            }
        )
    });
    $scope.send=function(){
        if($scope.msg){
            socket.emit('newMessage',$scope.msg);
            $scope.msg=null;
        }
    }
})